
public class Round {

	public static void main(String[] args) {
		//o metodo round retorna o inteiro de um double ou float, ajustado para cima ou para baixo
		double d = 302.200;
		double c = 302.700;
		System.out.println(Math.round(d));//retorna 302
		System.out.println(Math.round(c));//retorna 303
	}

}
