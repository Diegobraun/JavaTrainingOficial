
public class Abs {

	public static void main(String[] args) {
		//A função 'abs' retorna o valor absoluto, ou seja, como mostrado nos exemplos abaixo, o valor absoluto de -1, é 1
		Integer x = -1;
		double y = -2;
		float j = -100;
		System.out.println(Math.abs(x));//retorna 1
		System.out.println(Math.abs(y));//retorna 2.0
		System.out.println(Math.abs(j));//retorna 100.0
		
	}

}
