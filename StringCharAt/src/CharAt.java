
public class CharAt {

	public static void main(String[] args) {
		//o metodo charAt retorna um char no indice desejado de uma determinada string
		String x = "Olaa";//criando uma string
		char s = x.charAt(2);//guardando em uma variavel o indice 2 da var x
		System.out.println(s);//retorna 'a'
	}

}
