
public class FloorCeil {

	public static void main(String[] args) {
		//A função floor arredonda qualquer valor para baixo e o ceil arredonda para cima
		double d = 313.3;
		double c = 313.3;
		System.out.println(Math.ceil(d));//retorna 314.0
		System.out.println(Math.floor(c));//retorna 313.0
	}

}
